Uq<-function(A,beta,q,N){
if(q!=1){
	uq<-(((1/beta)^(1-q))-((1/N)^(1-q)))/(1-(1/N)^(1-q))
	}
else{
	tot<-sum(A)
	eA<-A/tot
	cs<-colSums(eA)
	lcs<-log(cs)
	x<-cs*lcs
	x<-x[x!="NaN"]
	hb=-(sum(x))
	pa=A/rowSums(A)
	x<-pa*log(pa)
	y<-rowSums(x,na.rm=T)
	z<-rowSums(A)/tot
	yz=y*z
	x<-yz[yz!="NaN"]
	ha=-(sum(x))
	wa=rowSums(A)/tot
	hc=sum(wa*log(wa))
	uq=(ha-hb-hc)/log(N)
	}
uq
}
Sq<-function(A,beta,q,N){
	sq<-((1/beta)-(1/N))/(1-1/N)
	sq
	}
Vq<-function(A,beta,q,N){
	vq<-1-((beta-1)/(N-1))
	vq
	}
