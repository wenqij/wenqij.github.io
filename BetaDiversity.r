d.chao<-function(A,lev,q)
{
	tot<-sum(A)
	eA<-A/tot
	eA<-eA[eA>0]
	if(is.vector(A)){
	cA<-A
	N<-1
	}else{
	cA<-colSums(A)
	N<-nrow(A)
}
ecA<-cA/tot
ecA<-ecA[ecA>0]
if(lev=='alpha'){
	if(q!=1){
	Da<-(1/N)*(sum(eA^q))^(1/(1-q))
	D.value<-Da
	}else{
Da<-exp(-sum(eA*log(eA))-log(N))
D.value<-Da
}
}
if(lev=='beta'){
	D.value<-d.chao(A,lev='gamma',q)/d.chao(A,lev='alpha',q)
	}
if(lev=='gamma'){
	if(q!=1){
	Dg<-(sum(ecA^q))^(1/(1-q))
	D.value<-Dg
	}else{
	Dg<-exp(-sum(ecA*log(ecA)))
	D.value<-Dg
	}
}
D.value
}
